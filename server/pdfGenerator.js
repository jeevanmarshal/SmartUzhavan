const generateDriverSalaryStatement = async (driver, salaryRecords, fromDate, toDate) => {
    return new Promise((resolve, reject) => {
        const doc = new PDFDocument({ margin: 50, size: 'A4' });
        const buffers = [];
        
        doc.on('data', buffers.push.bind(buffers));
        doc.on('end', () => resolve(Buffer.concat(buffers)));
        doc.on('error', reject);

        if (isFontValid(FONT_PATH)) {
            doc.font(FONT_PATH);
        } else {
            doc.font('Helvetica');
        }

        // Header
        doc.rect(0, 0, doc.page.width, 100).fill('#1B3A6B');
        doc.fillColor('white').fontSize(24).text('SMART UZHAVAN', 50, 30, { align: 'center' });
        doc.fontSize(12).text('Driver Salary Statement / ஓட்டுநர் சம்பள அறிக்கை', 50, 65, { align: 'center' });

        doc.fillColor('black').moveDown(4);

        // Driver Info
        doc.fontSize(10).fillColor('#1B3A6B').text('Driver Details / விவரம்', 50, 130);
        doc.fillColor('black').text(`Name: ${driver.name}`, 50, 150);
        doc.text(`ID: ${driver.id}`, 50, 165);
        doc.text(`Period: ${fromDate || 'Start'} to ${toDate || 'Present'}`, 350, 150, { align: 'right' });

        // Summary Totals
        const totalNet = salaryRecords.reduce((s, r) => s + (parseFloat(r.netPay) || 0), 0);
        const totalPaid = salaryRecords.reduce((s, r) => s + (r.payments || []).reduce((pSum, p) => pSum + (parseFloat(p.amount) || 0), 0), 0);
        const totalBalance = totalNet - totalPaid;

        doc.rect(50, 200, 150, 60).fill('#F7FAFC');
        doc.fillColor('black').fontSize(10).text('Total Payable', 60, 210);
        doc.fontSize(14).text(formatCurrency(totalNet), 60, 230);

        doc.rect(225, 200, 150, 60).fill('#F0FFF4');
        doc.fillColor('green').fontSize(10).text('Total Received', 235, 210);
        doc.fontSize(14).text(formatCurrency(totalPaid), 235, 230);

        doc.rect(400, 200, 150, 60).fill('#FFF5F5');
        doc.fillColor('red').fontSize(10).text('Balance / நிலுவை', 410, 210);
        doc.fontSize(14).text(formatCurrency(totalBalance), 410, 230);

        // Records Table
        doc.moveDown(8);
        doc.fillColor('#1B3A6B').fontSize(12).text('Salary History (வரிசைப்படியாக)');
        doc.moveDown();

        const tableY = 300;
        doc.fontSize(9).fillColor('black');
        doc.text('Date', 50, tableY);
        doc.text('Base', 120, tableY, { width: 60, align: 'right' });
        doc.text('Bonus', 180, tableY, { width: 60, align: 'right' });
        doc.text('Extra', 240, tableY, { width: 60, align: 'right' });
        doc.text('Adv', 300, tableY, { width: 60, align: 'right' });
        doc.text('Net Pay', 360, tableY, { width: 70, align: 'right' });
        doc.text('Paid', 430, tableY, { width: 60, align: 'right' });
        doc.text('Bal', 490, tableY, { width: 60, align: 'right' });

        let currentY = tableY + 20;
        salaryRecords.forEach(rec => {
            const paid = (rec.payments || []).reduce((sum, p) => sum + p.amount, 0);
            const bal = rec.netPay - paid;

            doc.fontSize(8);
            doc.text(rec.date, 50, currentY);
            doc.text(rec.baseSalary.toFixed(0), 120, currentY, { width: 60, align: 'right' });
            doc.text(rec.bonus.toFixed(0), 180, currentY, { width: 60, align: 'right' });
            doc.text(rec.extraAmount.toFixed(0), 240, currentY, { width: 60, align: 'right' });
            doc.text(rec.advance.toFixed(0), 300, currentY, { width: 60, align: 'right' });
            doc.text(rec.netPay.toFixed(0), 360, currentY, { width: 70, align: 'right' });
            doc.text(paid.toFixed(0), 430, currentY, { width: 60, align: 'right' });
            doc.text(bal.toFixed(0), 490, currentY, { width: 60, align: 'right' });

            currentY += 15;
            if (currentY > 700) {
                doc.addPage();
                currentY = 50;
            }
        });

        doc.fontSize(8).fillColor('#A0AEC0').text('Generated by SmartUzhavan Digital System', 50, 780, { align: 'center' });
        doc.end();
    });
};

